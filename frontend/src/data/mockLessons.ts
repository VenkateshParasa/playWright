// Helper function to get lessons by week
export function getLessonsByWeek(week: number): Lesson[] {
  return mockLessons.filter((lesson) => lesson.week === week);
}

// Helper function to get lessons by track
export function getLessonsByTrack(track: '30-day' | '60-day'): Lesson[] {
  return mockLessons.filter((lesson) => lesson.track === track || lesson.track === 'both');
}

// Helper function to get lesson by ID
export function getLessonById(id: string): Lesson | undefined {
  return mockLessons.find((lesson) => lesson.id === id);
}

// Helper function to calculate stats
export function calculateLessonStats(lessons: Lesson[]) {
  const stats = {
    totalLessons: lessons.length,
    completedLessons: lessons.filter((l) => l.status === 'completed').length,
    inProgressLessons: lessons.filter((l) => l.status === 'in-progress').length,
    lockedLessons: lessons.filter((l) => l.status === 'locked').length,
    averageProgress: 0,
    totalTimeSpent: 0,
  };

  stats.averageProgress = lessons.reduce((sum, l) => sum + l.progress, 0) / lessons.length;
  stats.totalTimeSpent = lessons
    .filter((l) => l.status === 'completed' || l.status === 'in-progress')
    .reduce((sum, l) => sum + l.estimatedDuration, 0);

  return stats;
}
